// Problem 1
// Determine the value of 'a' after each of the following expressions

public class Problem1 {
    public static void main(String[] args) {
        // Problem 1 a.
        int a = 3/2;
        System.out.println("The result of \'int a = 3/2;\' is a = " + a);
        // Problem 1 b.
        float b = 3/2;
        System.out.println("The result of \'float b = 3/2;\' is b = " + b);
        // Problem 1 c.
        float c = 3/2f;
        System.out.println("The result of \'float c = 3/2f;\' is c = " + c);
        // Problem 1 d.
        float d = (5*5-4)/6;
        System.out.println("The result of \'float d = (5*5-4)/6;\' is d = " + d);
        // Problem 1 e.
        float e = (float) 3/2;
        System.out.println("The result of \'float e = (float) 3/2;\' is e = " + e);
    }
}